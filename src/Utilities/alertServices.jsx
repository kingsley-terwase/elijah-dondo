import { toast } from "react-toastify"

export const toastSuccess = (message) => {
    toast.success(message, {
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined
    })
}

